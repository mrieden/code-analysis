"""Render result dicts into Markdown tables + a combined report."""
from __future__ import annotations

import os
from typing import Sequence

from .io_utils import RESULTS_DIR, ensure_results_dir


def pct(x) -> str:
    if not isinstance(x, (int, float)):
        return "n/a"
    return f"{100 * x:.1f}%"


def pct_ci(point, lo, hi) -> str:
    if not isinstance(point, (int, float)):
        return "n/a"
    return f"{100 * point:.1f}% [{100 * lo:.0f}-{100 * hi:.0f}]"


def md_table(headers: Sequence[str], rows: Sequence[Sequence[str]]) -> str:
    out = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out)


def append_section(md: str) -> None:
    ensure_results_dir()
    path = os.path.join(RESULTS_DIR, "REPORT.md")
    with open(path, "a", encoding="utf-8") as fh:
        fh.write(md.rstrip() + "\n\n")


def reset_report() -> None:
    ensure_results_dir()
    path = os.path.join(RESULTS_DIR, "REPORT.md")
    with open(path, "w", encoding="utf-8") as fh:
        fh.write("# CodeGuard - Evaluation Report\n\n")
        fh.write("_Generated by the drop-in eval harness. Systems compared per task._\n\n")
